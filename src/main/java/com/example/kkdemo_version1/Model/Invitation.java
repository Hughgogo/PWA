package com.example.kkdemo_version1.Model;


public class Invitation {
    private String invitations_id;
    private String invitations_senderId;
    private String invitations_senderName;
    private String invitations_icon;
    private String invitations_receiverEmail;
    private String invitations_status;
    private String invitations_type;

    public String getInvitations_id() {
        return invitations_id;
    }

    public void setInvitations_id(String invitations_id) {
        this.invitations_id = invitations_id;
    }

    public String getInvitations_senderId() {
        return invitations_senderId;
    }

    public void setInvitations_senderId(String invitations_senderId) {
        this.invitations_senderId = invitations_senderId;
    }

    public String getInvitations_senderName() {
        return invitations_senderName;
    }

    public void setInvitations_senderName(String invitations_senderName) {
        this.invitations_senderName = invitations_senderName;
    }

    public String getInvitations_icon() {
        return invitations_icon;
    }

    public void setInvitations_icon(String invitations_icon) {
        this.invitations_icon = invitations_icon;
    }

    public String getInvitations_receiverEmail() {
        return invitations_receiverEmail;
    }

    public void setInvitations_receiverEmail(String invitations_receiverEmail) {
        this.invitations_receiverEmail = invitations_receiverEmail;
    }

    public String getInvitations_status() {
        return invitations_status;
    }

    public void setInvitations_status(String invitations_status) {
        this.invitations_status = invitations_status;
    }

    public String getInvitations_type() {
        return invitations_type;
    }

    public void setInvitations_type(String invitations_type) {
        this.invitations_type = invitations_type;
    }
}
